public class Main {
    public static void main (String[] args) {

        // Her initialiseres en variabel med datatype int, myFirstNumber som navn, og assignes værdien 5
        int myFirstNumber = 5;

        // Her printes myFirstNumber ud - HUSK variabel navn skal bruges, da det er den der gemmer værdien
        System.out.println(myFirstNumber);

        // For at ændre myFirstNumber til anden værdi
        myFirstNumber = 10;

        System.out.println(myFirstNumber);

    }
}