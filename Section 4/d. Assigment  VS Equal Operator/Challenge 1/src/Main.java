public class Main {
    public static void main(String[] args) {

        // ------------- Korrekt brug af Equal Operator ---------- //

        int newValue = 50;

        if (newValue == 50)
        {
            System.out.println("This is an error");
        }


        // ------------- Not Equal Operator ---------- //

        boolean isCar = false;

        if (!isCar)
        {
            System.out.println("This is not supposed to happen");
        }

    }
}